package com.xds.express.widget;

import android.widget.TextView;

public class ExpressItem {
	private TextView company;
	private TextView status;
	private TextView pick_way;
	public TextView getCompany() {
		return company;
	}
	public void setCompany(TextView company) {
		this.company = company;
	}
	public TextView getStatus() {
		return status;
	}
	public void setStatus(TextView status) {
		this.status = status;
	}
	public TextView getPick_way() {
		return pick_way;
	}
	public void setPick_way(TextView pick_way) {
		this.pick_way = pick_way;
	}
	
}
